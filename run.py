"""
開発サーバーを起動するためのスクリプト

FastAPIアプリケーションを開発モードで起動する。
自動リロード機能が有効になっており、コードの変更が自動的に反映される。
"""
import uvicorn

if __name__ == "__main__":
    # host="0.0.0.0" にすることで、同じネットワーク内の他のデバイスからアクセス可能
    # これにより、実機や他のデバイスからもAPIにアクセスできる
    uvicorn.run(
        "app.main:app",
        host="0.0.0.0",  # すべてのネットワークインターフェースでリッスン（実機から接続可能）
        port=8000,        # ポート8000でリッスン
        reload=True,      # 自動リロードを有効化（開発時のみ）
    )



